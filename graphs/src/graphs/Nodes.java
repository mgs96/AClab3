package graphs;

import java.awt.Color;
import java.awt.Graphics;

/**
 *
 * @author mauri
 */
public class Nodes {
    
    private int x;
    private int y;
    private int radius;
    private Color color;
    
    public void draw(Graphics g){
        g.setColor(color);
        g.fillOval(this.x-radius, this.y-radius, radius*2, radius*2);
    }

    public Nodes(int x, int y, int radius, Color color) {
        this.x = x;
        this.y = y;
        this.radius = radius;
        this.color = color;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }
    
}


